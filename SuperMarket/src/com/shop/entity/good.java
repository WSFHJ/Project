package com.shop.entity;

public class good {
	private String number;
	private String CName;
	private String supplier;
	private String PArea;
	private String unit;
	private String shelfFife;
	private float price;
	private String pack;
	private float fullNumber;

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getCName() {
		return CName;
	}

	public void setCName(String cName) {
		CName = cName;
	}

	public String getSupplier() {
		return supplier;
	}

	public void setSupplier(String supplier) {
		this.supplier = supplier;
	}

	public String getPArea() {
		return PArea;
	}

	public void setPArea(String pArea) {
		PArea = pArea;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getShelfFife() {
		return shelfFife;
	}

	public void setShelfFife(String shelfFife) {
			this.shelfFife = shelfFife;	
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public String getPack() {
		return pack;
	}

	public void setPack(String pack) {
		this.pack = pack;
	}

	public float getFullNumber() {
		return fullNumber;
	}

	public void setFullNumber(float fullNumber) {
		this.fullNumber = fullNumber;
	}

}
